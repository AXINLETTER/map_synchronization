function [R_12] = helper_matrix_mul(R_1, R_2)
% This function computes the multiplcation of two rotation matrices
% in their vectorized form R12 = R_1*R_2

% R_1 is a 9xN vector, where N is the number of objects
% R_2 is a 9xN vector as well
% R_12 is a 9xN vector as well
R_12 = zeros(9, size(R_1, 2));
R_12(1,:) = R_1(1,:).*R_2(1,:) + R_1(4,:).*R_2(2,:) + R_1(7,:).*R_2(3,:);
R_12(2,:) = R_1(2,:).*R_2(1,:) + R_1(5,:).*R_2(2,:) + R_1(8,:).*R_2(3,:);
R_12(3,:) = R_1(3,:).*R_2(1,:) + R_1(6,:).*R_2(2,:) + R_1(9,:).*R_2(3,:);

R_12(4,:) = R_1(1,:).*R_2(4,:) + R_1(4,:).*R_2(5,:) + R_1(7,:).*R_2(6,:);
R_12(5,:) = R_1(2,:).*R_2(4,:) + R_1(5,:).*R_2(5,:) + R_1(8,:).*R_2(6,:);
R_12(6,:) = R_1(3,:).*R_2(4,:) + R_1(6,:).*R_2(5,:) + R_1(9,:).*R_2(6,:);

R_12(7,:) = R_1(1,:).*R_2(7,:) + R_1(4,:).*R_2(8,:) + R_1(7,:).*R_2(9,:);
R_12(8,:) = R_1(2,:).*R_2(7,:) + R_1(5,:).*R_2(8,:) + R_1(8,:).*R_2(9,:);
R_12(9,:) = R_1(3,:).*R_2(7,:) + R_1(6,:).*R_2(8,:) + R_1(9,:).*R_2(9,:);



